// Classe com hierarquia de classes
/**
 * Essa hierarquia de classes corrige todas as limitações das classes tagged
 * notadas anteriormente. O código é simples e claro, sem nenhum dos
 * elementos boilerplate encontrados no original. A implementação de cada
 * opção é atribuída à própria classe, e nenhuma dessas classes é
 * sobrecarregada por campos de dados irrelevantes.
 */
abstract class Figura {

    abstract double area();

}
